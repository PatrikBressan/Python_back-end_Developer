'''
- Polimorfismo: ter muitas formas;
- Em computação/progarmação:    polimorfismo significa o mesmo nome de função, mas com assinaturas diferentes,
                                sendo usado para tipos diferentes.
'''

# Mesmo nome de função, nesse caso a função len(); com assinaturas (tipos de dados) diferentes, retornando o tamanho da string/vetor/dados/etc.
print(len('Python'))
print(len([10, 20, 30]))